// ── Global extractors (parent scope, run on every response) ─

/**
 * Build extractors for correlations that apply across the whole journey
 * (CSRF tokens, session-scoped values). These go at the TestFragment/ThreadGroup
 * level so they run on every response — matching how the real NHS scripts work.
 */
function buildGlobalExtractors(correlations) {
  const lines = [];
  const globalTypes = new Set(['csrf', 'viewstate']);

  for (const corr of correlations) {
    if (!globalTypes.has(corr.type)) continue;
    // Use match_number 0 (random/any) like the real CovidBooking.jmx
    lines.push(`      <RegexExtractor guiclass="RegexExtractorGui" testclass="RegexExtractor" testname="Extract ${esc(corr.name)}" enabled="true">
        <stringProp name="RegexExtractor.useHeaders">false</stringProp>
        <stringProp name="RegexExtractor.refname">${esc(corr.name)}</stringProp>
        <stringProp name="RegexExtractor.regex">${esc(corr.extractRegex || '')}</stringProp>
        <stringProp name="RegexExtractor.template">$1$</stringProp>
        <stringProp name="RegexExtractor.default"></stringProp>
        <stringProp name="RegexExtractor.match_number">0</stringProp>
        <boolProp name="RegexExtractor.default_empty_value">false</boolProp>
      </RegexExtractor>
      <hashTree/>`);
  }

  return lines.join('\n');
}

// ── Step builders ───────────────────────────────────────────

function buildSteps(session, correlations, assertions) {
  const lines = [];
  const pad = session.options?.stepPadding || 2;

  for (const tx of session.transactions) {
    const stepLabel = `${session.journeyCode}-S${String(tx.stepNumber).padStart(pad + 1, '0')}`;
    const txName = tx.name || stepLabel;

    lines.push(`        <TransactionController guiclass="TransactionControllerGui" testclass="TransactionController" testname="${esc(txName)}" enabled="true">`);
    lines.push(`          <boolProp name="TransactionController.includeTimers">false</boolProp>`);
    lines.push(`        </TransactionController>`);
    lines.push(`        <hashTree>`);

    let samplerIdx = 0;
    for (const req of tx.requests) {
      samplerIdx++;
      const samplerName = `${String(samplerIdx).padStart(2, '0')} ${req.method} ${simplifyPath(req.path)}`;

      // Build the sampler
      lines.push(buildHttpSampler(session, req, samplerName, correlations));

      // Sampler's child elements (extractors, assertions)
      lines.push(`          <hashTree>`);

      // Extractors for correlations born in this response
      // Skip global types (csrf, viewstate) — they're at parent scope
      const globalTypes = new Set(['csrf', 'viewstate']);
      const bornHere = correlations.filter(c => {
        if (globalTypes.has(c.type)) return false; // handled globally
        if (c.sourceRequestSeq === req.seq) return true;
        if (c.allSources) return c.allSources.some(s => s.seq === req.seq);
        return false;
      });
      for (const corr of bornHere) {
        lines.push(buildExtractor(corr));
      }

      // Assertions for this request
      const reqAssertions = assertions.filter(a =>
        a.requestSeq === req.seq && a.transaction === tx.name
      );
      for (const assertion of reqAssertions) {
        lines.push(buildAssertion(assertion));
      }

      lines.push(`          </hashTree>`);
    }

    lines.push(`        </hashTree>`);
  }

  return lines.join('\n');
}

function buildHttpSampler(session, req, samplerName, correlations) {
  const parsed = new URL(req.url);
  const path = parsed.pathname + (parsed.search || '');
  const isPost = req.method === 'POST';
  const followRedirects = !isPost; // POSTs should not auto-follow in JMeter

  let argsXml = '';
  if (isPost && req.body) {
    argsXml = buildSamplerArguments(req, correlations);
  }

  return `          <HTTPSamplerProxy guiclass="HttpTestSampleGui" testclass="HTTPSamplerProxy" testname="${esc(samplerName)}" enabled="true">
            <elementProp name="HTTPsampler.Arguments" elementType="Arguments">
              <collectionProp name="Arguments.arguments">
${argsXml}
              </collectionProp>
            </elementProp>
            <stringProp name="HTTPSampler.domain">\${__P(TARGET_HOST,${esc(session.targetHost)})}</stringProp>
            <stringProp name="HTTPSampler.port">\${__P(TARGET_PORT,${esc(session.port)})}</stringProp>
            <stringProp name="HTTPSampler.protocol">${esc(session.protocol)}</stringProp>
            <stringProp name="HTTPSampler.contentEncoding">UTF-8</stringProp>
            <stringProp name="HTTPSampler.path">${esc(path)}</stringProp>
            <stringProp name="HTTPSampler.method">${req.method}</stringProp>
            <boolProp name="HTTPSampler.follow_redirects">${followRedirects}</boolProp>
            <boolProp name="HTTPSampler.auto_redirects">false</boolProp>
            <boolProp name="HTTPSampler.use_keepalive">true</boolProp>
            <boolProp name="HTTPSampler.DO_MULTIPART_POST">false</boolProp>
          </HTTPSamplerProxy>`;
}

function buildSamplerArguments(req, correlations) {
  const lines = [];
  let fields = [];

  if (req.bodyType === 'form-urlencoded' && req.body) {
    fields = parseFormBody(req.body);
  } else if (req.bodyType === 'json') {
    // JSON body — send as raw body
    lines.push(buildArgElement('', req.body, false));
    return lines.join('\n');
  }

  for (const field of fields) {
    // Check if this field is a correlation variable
    const corr = correlations.find(c => {
      if (c.type === 'csrf' || c.type === 'dynamic_id' || c.type === 'viewstate') {
        // Check if the value matches
        return field.value && c.sampleValue && field.value.includes(c.sampleValue.substring(0, 20));
      }
      return false;
    });

    // Check if this field is a data requirement (CSV parameterised)
    const isDataField = req.formFields?.some(f =>
      f.name === field.name && (f.classification === 'input' || f.classification === 'radio' || f.classification === 'select')
    );

    let value;
    if (corr) {
      value = `\${${corr.name}}`;
    } else if (isDataField) {
      const csvCol = toCsvColumn(field.name);
      value = `\${${csvCol}}`;
    } else {
      value = field.value;
    }

    lines.push(buildArgElement(field.name, value, true));
  }

  return lines.join('\n');
}

function buildArgElement(name, value, encode) {
  return `                <elementProp name="${esc(name)}" elementType="HTTPArgument">
                  <boolProp name="HTTPArgument.always_encode">${encode}</boolProp>
                  <stringProp name="Argument.value">${esc(value)}</stringProp>
                  <stringProp name="Argument.metadata">=</stringProp>
                  <boolProp name="HTTPArgument.use_equals">true</boolProp>
                  <stringProp name="Argument.name">${esc(name)}</stringProp>
                </elementProp>`;
}

// ── Extractors ──────────────────────────────────────────────

function buildExtractor(corr) {
  if (corr.extractType === 'jsonpath') {
    return `          <JSONPostProcessor guiclass="JSONPostProcessorGui" testclass="JSONPostProcessor" testname="Extract ${esc(corr.name)}" enabled="true">
            <stringProp name="JSONPostProcessor.referenceNames">${esc(corr.name)}</stringProp>
            <stringProp name="JSONPostProcessor.jsonPathExprs">${esc(corr.extractJsonPath)}</stringProp>
            <stringProp name="JSONPostProcessor.match_numbers">1</stringProp>
            <stringProp name="JSONPostProcessor.defaultValues">NOT_FOUND</stringProp>
          </JSONPostProcessor>
          <hashTree/>`;
  }

  // Default to regex extractor
  return `          <RegexExtractor guiclass="RegexExtractorGui" testclass="RegexExtractor" testname="Extract ${esc(corr.name)}" enabled="true">
            <stringProp name="RegexExtractor.useHeaders">false</stringProp>
            <stringProp name="RegexExtractor.refname">${esc(corr.name)}</stringProp>
            <stringProp name="RegexExtractor.regex">${esc(corr.extractRegex || '')}</stringProp>
            <stringProp name="RegexExtractor.template">$1$</stringProp>
            <stringProp name="RegexExtractor.default">NOT_FOUND</stringProp>
            <stringProp name="RegexExtractor.match_number">1</stringProp>
          </RegexExtractor>
          <hashTree/>`;
}

// ── Assertions ──────────────────────────────────────────────

function buildAssertion(assertion) {
  const scope = assertion.field === 'Assertion.response_code'
    ? `            <stringProp name="Assertion.test_field">Assertion.response_code</stringProp>`
    : `            <stringProp name="Assertion.test_field">Assertion.response_data</stringProp>`;

  // test_type: 2 = contains, 8 = equals, 1 = matches
  // Add 4 for NOT (so contains+not = 6)
  let testType = assertion.testType || 2;
  if (assertion.not) testType += 4;

  const label = assertion.not
    ? `Assert NOT ${assertion.type}: ${assertion.expected.substring(0, 30)}`
    : `Assert ${assertion.type}: ${assertion.expected.substring(0, 30)}`;

  return `          <ResponseAssertion guiclass="AssertionGui" testclass="ResponseAssertion" testname="${esc(label)}" enabled="true">
            <collectionProp name="Asserion.test_strings">
              <stringProp name="0">${esc(assertion.expected)}</stringProp>
            </collectionProp>
${scope}
            <boolProp name="Assertion.assume_success">false</boolProp>
            <intProp name="Assertion.test_type">${testType}</intProp>
          </ResponseAssertion>
          <hashTree/>`;
}

// ── CSV Data Sets ───────────────────────────────────────────

function buildCsvDataSets(session, dataReqs) {
  if (!dataReqs || dataReqs.length === 0) return '';

  const columns = dataReqs.map(d => d.suggestedCsvColumn);
  const uniqueColumns = [...new Set(columns)];
  const filename = `data/${session.journeyCode.toLowerCase()}_data.csv`;

  return `      <CSVDataSet guiclass="TestBeanGUI" testclass="CSVDataSet" testname="${esc(session.journeyCode)} Test Data" enabled="true">
        <stringProp name="filename">${esc(filename)}</stringProp>
        <stringProp name="fileEncoding">UTF-8</stringProp>
        <stringProp name="variableNames">${esc(uniqueColumns.join(','))}</stringProp>
        <stringProp name="delimiter">,</stringProp>
        <boolProp name="ignoreFirstLine">true</boolProp>
        <boolProp name="quotedData">true</boolProp>
        <boolProp name="recycle">true</boolProp>
        <boolProp name="stopThread">false</boolProp>
        <stringProp name="shareMode">shareMode.all</stringProp>
      </CSVDataSet>
      <hashTree/>
`;
}

// ── Think Timer ─────────────────────────────────────────────

function buildThinkTimer() {
  return `        <UniformRandomTimer guiclass="UniformRandomTimerGui" testclass="UniformRandomTimer" testname="Think Time" enabled="true">
          <stringProp name="ConstantTimer.delay">\${__P(minTime,${THINK_TIME.MIN})}</stringProp>
          <stringProp name="RandomTimer.range">\${__intSum(\${__P(maxTime,${THINK_TIME.MAX})},-\${__P(minTime,${THINK_TIME.MIN})})}</stringProp>
        </UniformRandomTimer>
        <hashTree/>`;
}

// ── Helpers ──────────────────────────────────────────────────

function esc(s) {
  if (s === null || s === undefined) return '';
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function simplifyPath(path) {
  if (!path) return '/';
  const clean = path.split('?')[0];
  const segments = clean.split('/').filter(Boolean);
  return segments.length > 0 ? segments[segments.length - 1] : '/';
}

function toCsvColumn(fieldName) {
  return fieldName
    .replace(/([a-z])([A-Z])/g, '$1_$2')
    .replace(/[^a-zA-Z0-9]/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_|_$/g, '')
    .toLowerCase() || 'field';
}
