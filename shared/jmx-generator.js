/**
 * JMX generation engine.
 *
 * Generates JMeter JMX XML from a recorded session. Two output modes:
 *
 * 1. Fragment — TestFragmentController for PerftestFramework IncludeController.
 *    Produces a UJ script (e.g. scripts/UJ01.jmx) that the orchestrator includes.
 *
 * 2. Standalone — Full TestPlan with thread group, HTTP defaults, cookie/cache
 *    managers. Ready to run directly in JMeter.
 *
 * JMX structure follows the conventions in INTEGRATION.md.
 */
import { THINK_TIME } from './constants.js';
import { parseFormBody } from '../background/recorder.js';

// ── Public API ──────────────────────────────────────────────

/**
 * Generate JMX XML string from a session.
 * @param {Object} session - Complete recorded session with analysis data
 * @param {Object} opts - { mode: 'fragment'|'standalone', journeyName: string }
 */
export function generateJmx(session, opts = {}) {
  const mode = opts.mode || 'fragment';
  const journeyName = opts.journeyName || `${session.journeyCode} - Recorded Journey`;
  const correlations = session.correlations || [];
  const dataReqs = session.dataRequirements || [];
  const assertions = session.assertions || [];

  const steps = buildSteps(session, correlations, assertions);
  const csvDataSets = buildCsvDataSets(session, dataReqs);
  const thinkTimer = buildThinkTimer();
  const globalExtractors = buildGlobalExtractors(correlations);

  if (mode === 'fragment') {
    return wrapFragment(session, journeyName, steps, csvDataSets, thinkTimer, globalExtractors);
  }
  return wrapStandalone(session, journeyName, steps, csvDataSets, thinkTimer, globalExtractors);
}

// ── Fragment mode ───────────────────────────────────────────

function wrapFragment(session, journeyName, steps, csvDataSets, thinkTimer, globalExtractors) {
  return `<?xml version="1.0" encoding="UTF-8"?>
<jmeterTestPlan version="1.2" properties="5.0" jmeter="5.6.3">
  <hashTree>
    <TestFragmentController guiclass="TestFragmentControllerGui" testclass="TestFragmentController" testname="${esc(journeyName)}" enabled="true"/>
    <hashTree>
      <CookieManager guiclass="CookiePanel" testclass="CookieManager" testname="HTTP Cookie Manager" enabled="true">
        <collectionProp name="CookieManager.cookies"/>
        <boolProp name="CookieManager.clearEachIteration">false</boolProp>
        <boolProp name="CookieManager.controlledByThreadGroup">false</boolProp>
      </CookieManager>
      <hashTree/>
${globalExtractors}
${csvDataSets}
      <TransactionController guiclass="TransactionControllerGui" testclass="TransactionController" testname="${esc(session.journeyCode)}_${esc(journeyName.replace(/^.*- /, ''))}" enabled="true">
        <boolProp name="TransactionController.includeTimers">false</boolProp>
        <boolProp name="TransactionController.parent">true</boolProp>
      </TransactionController>
      <hashTree>
${steps}
${thinkTimer}
      </hashTree>
    </hashTree>
  </hashTree>
</jmeterTestPlan>`;
}

